    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("MM/yy");
            String validade = dateFormat.format(textValidade.getDate());
            if (textId.getText().equals("") || textNome.getText().equals("") || textNumero.getText().equals("")||textLimite.getText().equals("")
                    || validade.equals("") || textFechamento.getText().equals("") || textPagamento.getText().equals("")
                    || textAgencia.getText().equals("") || textCvv.getText().equals("") || textBandeira.getText().equals("")) {
                throw new Exception("Algum dos campos está vazio");
            }

            int id = Integer.parseInt(textId.getText());
            if(textNumero.getText().length()<16){
                throw new Exception("O número de um cartão tem 16 dígitos");
            }
            String numero = textNumero.getText();           
            
            String nome = textNome.getText();                  
            int diaDoFechamento = Integer.parseInt(textFechamento.getText());
            int diaDoPagamento = Integer.parseInt(textPagamento.getText());
            int cvv = Integer.parseInt(textCvv.getText());
            String bandeira = textBandeira.getText();
            String aux1 = textLimite.getText().replaceAll(",", ".");
            double limite = Double.parseDouble(aux1);
            int agencia = Integer.parseInt(textAgencia.getText());
            String conta = comboBoxConta.getSelectedItem() + "";

            aux1 = textTaxa.getText().replaceAll("%", "");
            aux1 = aux1.replaceAll(",", ".");
            double taxa = Double.parseDouble(aux1);

            Cartao aux = new Cartao(id, numero, nome, validade, diaDoFechamento, diaDoPagamento, cvv, bandeira,
                    limite, agencia, conta, taxa);
            cControle.incluir(aux);

            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        try {
            String str = textId.getText();
            if (str.length() == 0) {
                throw new Exception("Identificador da tela está vazio");
            }
            int id = Integer.parseInt(str);
            Cartao aux = cControle.consultarPorId(id);
            if (aux == null) {
                throw new Exception("Cartão não existe");
            }

            textId.setText(aux.getId() + "");
            for (int i = 0; i < comboBoxConta.getItemCount(); i++) {
                String x1 = comboBoxConta.getItemAt(i);
                if (x1.equals(aux.getConta())) {
                    comboBoxConta.setSelectedItem(x1);
                }
            }
            
            textNumero.setText(aux.getNumero() + "");
            textNome.setText(aux.getNome());
            Date date1 = new SimpleDateFormat("MM/yy").parse(aux.getValidade());
            textValidade.setDate(date1);
            textFechamento.setText(aux.getDiaDoFechamento() + "");
            textPagamento.setText(aux.getDiaDoPagamento() + "");
            textCvv.setText(aux.getCvv() + "");
            textBandeira.setText(aux.getBandeira());
            DecimalFormat df2 = new DecimalFormat("#,##0.00");
            textLimite.setText(df2.format(aux.getLimite()));
            textAgencia.setText(aux.getAgencia() + "");
            textTaxa.setText(df2.format(aux.getTaxa()) + "%");

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        try {
            String str1 = textId.getText();
            SimpleDateFormat dateFormat = new SimpleDateFormat("MM/yy");
            String validade = dateFormat.format(textValidade.getDate());
            if (textId.getText().equals("") || textNome.getText().equals("") || textNumero.getText().equals("")||textLimite.getText().equals("")
                    || validade.equals("") || textFechamento.getText().equals("") || textPagamento.getText().equals("")
                    || textAgencia.getText().equals("") || textCvv.getText().equals("") || textBandeira.getText().equals("")) {
                throw new Exception("Algum dos campos está vazio");
            }

            if (str1.length() == 0) {
                throw new Exception("Identificador da tela está vazio");
            }

            Cartao aux = new Cartao();

            aux.setId(Integer.parseInt(str1));
            if(textNumero.getText().length()<16){
                throw new Exception("O número de um cartão tem 16 dígitos");
            }
            aux.setNumero(textNumero.getText());
            aux.setNome(textNome.getText());            
            aux.setValidade(dateFormat.format(textValidade.getDate()));
            aux.setDiaDoFechamento(Integer.parseInt(textFechamento.getText()));
            aux.setDiaDoPagamento(Integer.parseInt(textPagamento.getText()));
            aux.setCvv(Integer.parseInt(textCvv.getText()));
            aux.setBandeira(textBandeira.getText());
            String aux1 = textLimite.getText().replaceAll(",", ".");
            aux.setLimite(Double.parseDouble(aux1));
            aux.setAgencia(Integer.parseInt(textAgencia.getText()));
            aux.setConta(comboBoxConta.getSelectedItem() + "");

            aux1 = textTaxa.getText().replaceAll("%", "");
            aux1 = aux1.replaceAll(",", ".");
            aux.setTaxa(Double.parseDouble(aux1));

            cControle.alterar(Integer.parseInt(str1), aux);
            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

