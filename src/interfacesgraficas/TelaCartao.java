    private void mostrarListagem() {
        try {
            ArrayList<Cartao> aux = cControle.listagem();
            DefaultTableModel model = (DefaultTableModel) tableCartoes.getModel();
            model.setNumRows(0);
            for (int pos = 0; pos < aux.size(); pos++) {
                String[] saida = new String[12];
                Cartao c1 = aux.get(pos);
                DecimalFormat df = new DecimalFormat("R$#,##0.00");
                DecimalFormat df1 = new DecimalFormat("#,###0.00");

                saida[0] = c1.getId() + "";
                String number = c1.getNumero();                
                number = number.substring(0, 4) + "-" + number.substring(4, 8) + "-" + number.substring(8, 12) + "-" + number.substring(12, 16);
                saida[1] = number;
                saida[2] = c1.getNome() + "";
                saida[3] = c1.getValidade() + "";
                saida[4] = c1.getDiaDoFechamento() + "";
                saida[5] = c1.getDiaDoPagamento() + "";
                saida[6] = c1.getCvv() + "";
                saida[7] = c1.getBandeira() + "";
                String sup= c1.getLimite()+"";
                if(sup.equals("0.0")){
                    saida[8]="Cartão de débito";
                }
                else{
                    saida[8] = df.format(c1.getLimite());
                }                
                saida[9] = c1.getAgencia() + "";
                saida[10] = c1.getConta() + "";
                saida[11] = df1.format(c1.getTaxa()) + "%";

                //incluir nova linha na tabela
                model.addRow(saida);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }

