    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextFieldIdentificador.getText().equals("") || jTextFieldDescricao.getText().equals("")) {
                throw new Exception("Algum dos campos está vazio");
            }
            int id = Integer.parseInt(jTextFieldIdentificador.getText());
            String nomeDoBanco = jTextFieldDescricao.getText();
            Banco objBanco = new Banco(id, nomeDoBanco);

            objBancoControle.incluir(objBanco);
            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonConsultarPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarPorIdActionPerformed
        // TODO add your handling code here:
        try {
            String str = jTextFieldIdentificador.getText();
            if (str.length() == 0) {
                throw new Exception("Identificador da tela esta vazio");
            }
            int id = Integer.parseInt(str);
            Banco objBanco = objBancoControle.consultarPorId(id);
            if (objBanco == null) {
                throw new Exception("Banco não existe");
            }
            jTextFieldIdentificador.setText(objBanco.getId() + "");
            jTextFieldDescricao.setText(objBanco.getDescricao());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultarPorIdActionPerformed

    private void jButtonExcluirPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirPorIdActionPerformed
        // TODO add your handling code here:
        try {
            String str = jTextFieldIdentificador.getText();
            if (str.length() == 0) {
                throw new Exception("Identificador da tela esta vazio");
            }
            int id = Integer.parseInt(str);
            objBancoControle.apagarPorId(id);
            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_jButtonExcluirPorIdActionPerformed

    private void botaoAlterarBancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarBancoActionPerformed
        try {
            String str1 = jTextFieldIdentificador.getText();
            String str2 = jTextFieldDescricao.getText();
            if (str1.length() == 0||str2.length() == 0) {
                throw new Exception("Algum dos campos não está preenchido");
            }
            
            int id = Integer.parseInt(str1);            
            Banco aux = new Banco();
            aux.setId(id);
            aux.setDescricao(str2);
            objBancoControle.alterarPorId(id, aux);
            limparTela();
            mostrarListagem();
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }//GEN-LAST:event_botaoAlterarBancoActionPerformed

