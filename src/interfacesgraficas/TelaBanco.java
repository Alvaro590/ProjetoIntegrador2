    private void mostrarListagem() {
        try {
            ArrayList<Banco> array = objBancoControle.listagem();
            DefaultTableModel model = (DefaultTableModel) jTableBancos.getModel();
            model.setNumRows(0);
            for (int pos = 0; pos < array.size(); pos++) {
                String[] saida = new String[2];
                Banco aux = array.get(pos);
                saida[0] = aux.getId() + "";
                saida[1] = aux.getDescricao();
                //incluir nova linha na tabela
                model.addRow(saida);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }

